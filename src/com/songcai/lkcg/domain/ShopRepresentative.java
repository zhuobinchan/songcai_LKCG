package com.songcai.lkcg.domain;

public class ShopRepresentative {
	private Long sr_id;
	private Shop shop;
	private String sr_name;
	private String sr_identity_card;
	private String sr_telephone;
	private String sr_password;
	private String sr_bank_card;

	public Long getSr_id() {
		return sr_id;
	}

	public void setSr_id(Long sr_id) {
		this.sr_id = sr_id;
	}

	public Shop getShop() {
		return shop;
	}

	public void setShop(Shop shop) {
		this.shop = shop;
	}

	public String getSr_name() {
		return sr_name;
	}

	public void setSr_name(String sr_name) {
		this.sr_name = sr_name;
	}

	public String getSr_identity_card() {
		return sr_identity_card;
	}

	public void setSr_identity_card(String sr_identity_card) {
		this.sr_identity_card = sr_identity_card;
	}

	public String getSr_telephone() {
		return sr_telephone;
	}

	public void setSr_telephone(String sr_telephone) {
		this.sr_telephone = sr_telephone;
	}

	public String getSr_password() {
		return sr_password;
	}

	public void setSr_password(String sr_password) {
		this.sr_password = sr_password;
	}

	public String getSr_bank_card() {
		return sr_bank_card;
	}

	public void setSr_bank_card(String sr_bank_card) {
		this.sr_bank_card = sr_bank_card;
	}

}
