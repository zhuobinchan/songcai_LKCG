package com.songcai.lkcg.domain;

import java.util.HashSet;
import java.util.Set;

public class Order {
	private Long order_id;
	private Customer customer;
	private String order_consignee;
	private String order_address;
	private String order_time;
	private String order_introduce;
	private double order_total_price;
	private int order_state;
	private String order_telephone;
	private Set<OrderDetail> orderDetails = new HashSet<OrderDetail>();

	public Long getOrder_id() {
		return order_id;
	}

	public void setOrder_id(Long order_id) {
		this.order_id = order_id;
	}

	public Customer getCustomer() {
		return customer;
	}

	public void setCustomer(Customer customer) {
		this.customer = customer;
	}

	public String getOrder_consignee() {
		return order_consignee;
	}

	public void setOrder_consignee(String order_consignee) {
		this.order_consignee = order_consignee;
	}

	public String getOrder_address() {
		return order_address;
	}

	public void setOrder_address(String order_address) {
		this.order_address = order_address;
	}

	public String getOrder_time() {
		return order_time;
	}

	public void setOrder_time(String order_time) {
		this.order_time = order_time;
	}

	public String getOrder_introduce() {
		return order_introduce;
	}

	public void setOrder_introduce(String order_introduce) {
		this.order_introduce = order_introduce;
	}

	public double getOrder_total_price() {
		return order_total_price;
	}

	public void setOrder_total_price(double order_total_price) {
		this.order_total_price = order_total_price;
	}

	public int getOrder_state() {
		return order_state;
	}

	public void setOrder_state(int order_state) {
		this.order_state = order_state;
	}

	public String getOrder_telephone() {
		return order_telephone;
	}

	public void setOrder_telephone(String order_telephone) {
		this.order_telephone = order_telephone;
	}

	public Set<OrderDetail> getOrderDetails() {
		return orderDetails;
	}

	public void setOrderDetails(Set<OrderDetail> orderDetails) {
		this.orderDetails = orderDetails;
	}

}
